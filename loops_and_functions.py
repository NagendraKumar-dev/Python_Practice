# 1️⃣ For loop example
fruits = ["apple", "banana", "cherry"]
for fruit in fruits:
    print("I like", fruit)

# 2️⃣ While loop example
count = 1
while count <= 5:
    print("Count =", count)
    count += 1

# 3️⃣ Function example
def greet(name):
    return f"Hello, {name}! Welcome to Python."

# Call function
message = greet("Nagendra")
print(message)

# 4️⃣ Function with parameters
def add_numbers(a, b):
    return a + b

print("Sum =", add_numbers(10, 20))
